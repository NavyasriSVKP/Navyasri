package BasicsofJava;

public class AssignmentOperator {

	public static void main(String[] args) {
		int a=6;
		//Add and assign
		a+=8;
		System.out.println("a after +=" +a);
		//Sub and assign
		a-=5;
		System.out.println("a after -=" +a);
		//Multiply and assign
		a*=3;
		System.out.println("a after *=" +a);
		//Divide and assign
		a/=2;
		System.out.println("a after /=" +a);
		//Modulus and assign
		a%=4;
		System.out.println("a after %=" +a);
	}

}
